package chapter02.practice;

public class p1 {
  public static void main(String[] args) {
    // 練習變量之整型 byte short int long
    byte b1 = 127;
    short s1 = 12121;
    int i1 = 241241;
    long l1 = 12894918641L;
    
    // 練習變量之浮點型 double float
    double d1 = 3.1415;
    float f1 = 3.14F;
    
    // 練習變量之字符型 char boolean
    char c1 = '1';
    char c2 = 1;
    System.out.println(c1 == c2); // false
    char c3 = 'h';
    char c4 = '&';
    char c5 = '嗨';
    boolean b2 = false;
    boolean b3 = true;
  }
}
